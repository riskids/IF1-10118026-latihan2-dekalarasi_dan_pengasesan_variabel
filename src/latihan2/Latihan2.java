public class Latihan2 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // deklarasi variable
        int nilaiInt;
        final double PHI = 3.4;//konstanta uppercase
        boolean nilaiLogika;
        char nilaiKarakter;
        
        //memberi nilai ke variable
        nilaiInt = 21;
        nilaiLogika = true;
        nilaiKarakter = 'E';
        
        //menampilkan hasil
        System.out.println();
        System.out.println("Isi variabel nilai = " + nilaiInt);
        System.out.println("Isi Variabel PHI = " + PHI);
        System.out.println("Isi variabel logika = "+ nilaiLogika);
        System.out.println("Isi Variabel Karakter = " + nilaiKarakter);
        
    }
    
}
